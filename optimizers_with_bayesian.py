def black_box(mu0, mu1, x):
    mu_modified = mu.copy()
    mu_modified[0] = mu0
    mu_modified[1] = mu1

    A_values = A(mu_modified)
    rho_values = rho(x, mu_modified)

    term1 = sum(mu_modified[i]**2 * mu_modified[j]**2 * A_values[i] * A_values[j] * rho_values[i] * rho_values[j] *
                (np.sin(mu_modified[i]+mu_modified[j])/(mu_modified[i]+mu_modified[j]) +
                 np.sin(mu_modified[i]-mu_modified[j])/(mu_modified[i]-mu_modified[j]))
                for i in range(30) for j in range(i+1, 30))

    term2 = sum(mu_modified[j]**4 * A_values[j]**2 * rho_values[j]**2 *
                (np.sin(2*mu_modified[j])/(2*mu_modified[j]) + 1)/2
                for j in range(30))

    term3 = sum(mu_modified[j]**2 * A_values[j] * rho_values[j] *
                (2*np.sin(mu_modified[j])/mu_modified[j]**3 -
                 2*np.cos(mu_modified[j])/mu_modified[j]**2)
                for j in range(30))

    return (term1 + term2 - term3 + 2/15 - 0.0001)

def constraint(x, mu, A_values):
    """Calculate the constraint function value with using bayesian optimization. It returns - maximum since we use SLSQP. """
    # This bound shows parameters change bound. In this case we give +-%10 uncertainity for parameters.
    pbounds = {
        'mu0': (mu[0] * 0.9, mu[0] * 1.1),
        'mu1': (mu[1] * 0.9, mu[1] * 1.1)
    }  # Optimize only selected indices


    #bo_wrapper is used for fixing x. It is necessery for bayesian optimization library that we used. "Learned from chatgpt."
    def bo_wrapper(mu0, mu1):
        return black_box(mu0, mu1, x)

    optimizer = BayesianOptimization(f=bo_wrapper, pbounds=pbounds, verbose = 0)

    # Run Bayesian Optimization
    optimizer.maximize(init_points=5, n_iter=10)
    # Return negative maximum for SLSQP
    return -1 * optimizer.max["target"]

