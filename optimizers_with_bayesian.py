def objective(x):
    """Calculate the objective function value."""
    return np.sum(x**2)

def rho(x, mu_vals):
    """Calculate the rho function for given x and mu values."""
    n = len(x)
    result = np.zeros(len(mu_vals))
    for j in range(len(mu_vals)):
        exp_term = np.exp(-mu_vals[j]**2 * np.sum(x**2))
        sum_term = sum(2 * (-1)**(ii-1) * np.exp(-mu_vals[j]**2 * np.sum(x[ii-1:]**2))
                     for ii in range(2, n+1))
        result[j] = -(exp_term + sum_term + (-1)**n) / mu_vals[j]**2
    return result

def A(mu_val):
    """Calculate the A function for a given mu value."""
    return 2 * np.sin(mu_val) / (mu_val + np.sin(mu_val) * np.cos(mu_val))

